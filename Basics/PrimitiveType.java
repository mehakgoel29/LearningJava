public class PrimitiveType {
    public static void main(String[] args) {
        //primitive datatypes are the datatpes that can not be further divided.for example: string "Mehak" can be divided 
        //into char datatype : 'm','e','h','a','k'...so string is not a primitive datatype
        int num=23;
        char character='a';
        float marks=90.0f;
        double largeDecimalNumber=34556.909;
        long largeInterger=546898098765435668L;
        boolean check=true;
        System.out.println(num);
        System.out.println(character);
        System.out.println(marks);
        System.out.println(largeDecimalNumber);
        System.out.println(largeInterger);
        System.out.println(check);
    }
    
}
